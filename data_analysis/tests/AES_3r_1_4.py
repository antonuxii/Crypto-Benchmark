def bv8(x):
    return BitVecVal(x,8)

BV8=BitVecSort(8)

sbox = K(BV8, bv8(0))
for i,v in enumerate(SBOX):
    sbox = Store(sbox, bv8(i), bv8(v))


def xtime(b):
    return If((b & bv8(0x80)) == bv8(0), (b << 1) & bv8(0xff), ((b << 1) & bv8(0xff)) ^ bv8(0x1b))

def mul(b,c):
    if c==1: return b
    if c==2: return xtime(b)
    if c==3: return xtime(b) ^ b
    raise ValueError


def sub_bytes(st):
    return [Select(sbox,b) for b in st]

def shift_rows(st):
    out=[None]*16
    for r in range(4):
        for c in range(4):
            out[r+4*c]=st[r+4*((c+r)%4)]
    return out

def mix_single_column(col):
    a0,a1,a2,a3=col
    return [mul(a0,2)^mul(a1,3)^a2^a3,
            a0^mul(a1,2)^mul(a2,3)^a3,
            a0^a1^mul(a2,2)^mul(a3,3),
            mul(a0,3)^a1^a2^mul(a3,2)]

def mix_columns(st):
    out=[None]*16
    for c in range(4):
        col=[st[0+4*c],st[1+4*c],st[2+4*c],st[3+4*c]]
        mcol=mix_single_column(col)
        for r in range(4):
            out[r+4*c]=mcol[r]
    return out

def add_round_key(st,rk):
    return [st[i]^rk[i] for i in range(16)]

def rot_word(w):
    return w[1:]+w[:1]

def sub_word(w):
    return [Select(sbox,b) for b in w]

def expand_key_128(kbytes):
    w=[kbytes[4*i:4*i+4] for i in range(4)]
    for i in range(4,16):
        temp=w[i-1]
        if i%4==0:
            temp=sub_word(rot_word(temp))
            temp=[temp[0]^bv8(RCON[i//4]),temp[1],temp[2],temp[3]]
        w.append([w[i-4][j]^temp[j] for j in range(4)])
    rks=[]
    for r in range(4):
        rk=[]
        for i in range(4):
            rk += w[4*r+i]
        rks.append(rk)
    return rks

def aes3_encrypt_z3(pt,keybytes):
    rks=expand_key_128(keybytes)
    st=add_round_key(pt,rks[0])
    # rounds 1,2
    for rnd in [1,2]:
        st=sub_bytes(st)
        st=shift_rows(st)
        st=mix_columns(st)
        st=add_round_key(st,rks[rnd])
    # round 3 final
    st=sub_bytes(st)
    st=shift_rows(st)
    st=add_round_key(st,rks[3])
    return st


